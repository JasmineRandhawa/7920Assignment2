package com.example._7920Assignment2;

/* store coordinates of a paint on a path*/
public class PathPoint {
        float x, y;

        // constructor
        public PathPoint(float x, float y) {
            this.x = x;
            this.y = y;
        }

        //getters and setters for Path Point vlas fields
        public float getX() {
            return x;
        }

        public float getY() {
            return y;
    }
}
