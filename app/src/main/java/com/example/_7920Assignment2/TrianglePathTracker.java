package com.example._7920Assignment2;

/* Saves history of Object Paths drawn*/
public class TrianglePathTracker {
    int StartX;
    int StartY;
    int EndX;
    int EndY;

    // Constructor of Path Tracker class
    public TrianglePathTracker( int startX, int startY, int endX, int endY) {
        StartX = startX;
        StartY = startY;
        EndX = endX;
        EndY = endY;
    }
}
